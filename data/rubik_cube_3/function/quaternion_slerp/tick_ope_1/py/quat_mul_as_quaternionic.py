#四元数乘法

import quaternionic
import numpy

def quat_mul(q1,q2):
    #quaternionic定义的四元数顺序： [w, x, y, z]
    r1 = quaternionic.array([q1[3], q1[0], q1[1], q1[2]])
    r2 = quaternionic.array([q2[3], q2[0], q2[1], q2[2]])
    r3 = r1 * r2
    r3_nd = r3.ndarray   # 得到普通 numpy 数组，形状为 (4,)
    return numpy.array([r3_nd[1], r3_nd[2], r3_nd[3], r3_nd[0]], dtype=float)  #把顺序调整为[x, y, z, w]

q1 = [0.0, 0.9808891200000001, 0.0, -0.19522784]
q2 = [-0.44270834, 0.114583336, 0.50033337, 0]
print(quat_mul(q1,q2))

q1 = [0.57720053, -0.022369860000000002, 0.3365688, -0.11239354]
q2 = [0.0, -0.9808891200000001, 0.0, -0.19522784]
print(quat_mul(q1,q2))


